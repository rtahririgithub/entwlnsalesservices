package com.telus.csm.ewlnsms.core.domain;

public class ProductAttributes {
	
	private String productComponentName;
	private String productComponentCatalogId;
	private String productComponentCatalogAttributeId;
	private String productComponentAttributeName;
	
	public String getProductComponentCatalogId() {
		return productComponentCatalogId;
	}
	public void setProductComponentCatalogId(String productComponentCatalogId) {
		this.productComponentCatalogId = productComponentCatalogId;
	}
	public String getProductComponentName() {
		return productComponentName;
	}
	public void setProductComponentName(String productComponentName) {
		this.productComponentName = productComponentName;
	}
	public String getProductComponentCatalogAttributeId() {
		return productComponentCatalogAttributeId;
	}
	public void setProductComponentCatalogAttributeId(String productComponentCatalogAttributeId) {
		this.productComponentCatalogAttributeId = productComponentCatalogAttributeId;
	}
	public String getProductComponentAttributeName() {
		return productComponentAttributeName;
	}
	public void setProductComponentAttributeName(String productComponentAttributeName) {
		this.productComponentAttributeName = productComponentAttributeName;
	}
	
	
	

}
